code(
    "def make_gnn_dataset(n_samples=800, noise=0.3):\n"
    "    base = triangular_lattice(3, 3)\n"
    "    rng = np.random.default_rng(7)\n"
    "    configs, energies = [], []\n"
    "    while len(energies) < n_samples:\n"
    "        pos = base + noise * rng.standard_normal(base.shape)\n"
    "        E = lj_energy(pos)\n"
    "        if abs(E) < 50:\n"
    "            configs.append(pos.copy()); energies.append(E)\n"
    "    return configs, np.array(energies)\n"
    "\n"
    "print('Generating GNN dataset...')\n"
    "configs, energies = make_gnn_dataset()\n"
    "print(f'{len(configs)} configs')"
)

code(
    "n_val = 150\n"
    "n_train = len(configs) - n_val\n"
    "train_configs, train_E = configs[:n_train], energies[:n_train]\n"
    "val_configs, val_E = configs[n_train:], energies[n_train:]\n"
    "\n"
    "opt = torch.optim.Adam(gnn.parameters(), lr=1e-3)\n"
    "sched = torch.optim.lr_scheduler.CosineAnnealingLR(opt, T_max=200)\n"
    "\n"
    "train_losses, val_losses = [], []\n"
    "print('Training GNN (this may take ~2 minutes on CPU)...')\n"
    "for epoch in range(200):\n"
    "    gnn.train()\n"
    "    perm = np.random.permutation(n_train)\n"
    "    epoch_loss = 0.0\n"
    "    for b_start in range(0, n_train, 32):\n"
    "        opt.zero_grad()\n"
    "        batch_loss = 0.0\n"
    "        batch_idx = perm[b_start:b_start + 32]\n"
    "        for idx in batch_idx:\n"
    "            pos_t = torch.tensor(train_configs[idx], dtype=torch.float32)\n"
    "            batch_loss = batch_loss + (gnn(pos_t) - train_E[idx]) ** 2\n"
    "        batch_loss = batch_loss / len(batch_idx)\n"
    "        batch_loss.backward()\n"
    "        opt.step()\n"
    "        epoch_loss += batch_loss.item()\n"
    "    train_losses.append(epoch_loss / (n_train / 32))\n"
    "    gnn.eval()\n"
    "    with torch.no_grad():\n"
    "        vl = sum((gnn(torch.tensor(c, dtype=torch.float32)).item() - e) ** 2\n"
    "                 for c, e in zip(val_configs, val_E)) / n_val\n"
    "    val_losses.append(vl); sched.step()\n"
    "    if (epoch + 1) % 50 == 0:\n"
    "        print(f'epoch {epoch+1:3d}  train MSE={train_losses[-1]:.4f}  val MSE={val_losses[-1]:.4f}')\n"
    "\n"
    "plt.figure(figsize=(6, 3))\n"
    "plt.semilogy(train_losses, label='train')\n"
    "plt.semilogy(val_losses, label='val')\n"
    "plt.xlabel('epoch'); plt.ylabel('MSE'); plt.title('GNN training curve')\n"
    "plt.legend(); plt.tight_layout(); plt.show()"
)

code(
    "def gnn_forces(pos_np):\n"
    "    pos_t = torch.tensor(pos_np, dtype=torch.float32, requires_grad=True)\n"
    "    E = gnn(pos_t)\n"
    "    E.backward()\n"
    "    return -pos_t.grad.numpy()\n"
    "\n"
    "def gnn_energy(pos_np):\n"
    "    with torch.no_grad():\n"
    "        return gnn(torch.tensor(pos_np, dtype=torch.float32)).item()\n"
    "\n"
    "# Force-gradient check (now runs -- forces are no longer None)\n"
    "pos_test = triangular_lattice(3, 3) + 0.05 * np.random.randn(9, 2)\n"
    "F_analytic = gnn_forces(pos_test)\n"
    "h = 1e-3\n"
    "F_numerical = np.zeros_like(pos_test)\n"
    "for i in range(pos_test.shape[0]):\n"
    "    for k in range(pos_test.shape[1]):\n"
    "        pp, pm = pos_test.copy(), pos_test.copy()\n"
    "        pp[i, k] += h; pm[i, k] -= h\n"
    "        F_numerical[i, k] = -(gnn_energy(pp) - gnn_energy(pm)) / (2 * h)\n"
    "print(f'GNN force-gradient check: max err = {np.max(np.abs(F_analytic - F_numerical)):.5f}')\n"
    "print(f'autograd == numerical: {np.allclose(F_analytic, F_numerical, atol=1e-2)}')\n"
    "\n"
    "print('Running GNN-MD...')\n"
    "start_pos = triangular_lattice(3, 3)\n"
    "start_vel = thermal_velocities(9, d=2, temp=0.1, seed=3)\n"
    "pos = start_pos.copy(); vel = start_vel.copy()\n"
    "forces = gnn_forces(pos)\n"
    "tot_E, dt, steps = [], 0.003, 300\n"
    "for _ in range(steps):\n"
    "    v_half = vel + 0.5 * forces * dt\n"
    "    pos = pos + v_half * dt\n"
    "    forces = gnn_forces(pos)\n"
    "    vel = v_half + 0.5 * forces * dt\n"
    "    tot_E.append(0.5 * np.sum(vel ** 2) + gnn_energy(pos))\n"
    "\n"
    "drift = (max(tot_E) - min(tot_E)) / abs(np.mean(tot_E))\n"
    "print(f'GNN-MD energy drift: {drift:.3%}')\n"
    "plt.figure(figsize=(6, 3))\n"
    "plt.plot(tot_E)\n"
    "plt.xlabel('MD step'); plt.ylabel('total energy')\n"
    "plt.title(f'GNN-MD energy conservation (drift={drift:.2%})')\n"
    "plt.tight_layout(); plt.show()"
)

