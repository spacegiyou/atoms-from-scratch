"""Build notebooks/05_message_passing_graph_neural_nets.ipynb  (CORRECTED)

Fix vs original: GNNPotential.forward previously did `pos_np = pos_t.detach().numpy()`
and computed all edge distances/RBF features in NumPy, detached from the graph. The
energy therefore had NO gradient dependence on pos_t, so `gnn_forces` computed
`-pos_t.grad.numpy()` with `pos_t.grad is None` -> AttributeError (crash) at the
force-gradient check and the MD cell.

The fix: build the neighbour-list *indices* on detached positions (membership is a
step function, treated as fixed), but recompute the edge displacement vectors and
distances IN TORCH from pos_t, and expand them with a torch RBF. Now the energy
depends on pos_t and autograd yields correct, finite forces.
"""

