# ---------------------------------------------------------------------------
md(
    "## 1. What Lennard-Jones assumes\n"
    "\n"
    "The LJ potential:\n"
    "\n"
    "$$V_{LJ}(r) = 4\\varepsilon\\left[\\left(\\frac{\\sigma}{r}\\right)^{12}"
    " - \\left(\\frac{\\sigma}{r}\\right)^{6}\\right]$$\n"
    "\n"
    "embeds four assumptions:\n"
    "\n"
    "1. **Pairwise additivity.** Total energy = sum over pairs. No three-body or many-body effects.\n"
    "2. **Spherical symmetry.** Only distance $r$ matters, not direction. No preferred angles.\n"
    "3. **Fixed functional form.** The 12-6 exponents are fixed. The shape cannot adapt.\n"
    "4. **No bond breaking.** There is a single well; separating atoms just makes the potential go to zero.\n"
    "\n"
    "For noble gases like argon these are fine. For everything else they fail in different ways."
)

code(
    "# Compare LJ to a Morse potential (more bond-like)\n"
    "# Morse has a finite dissociation energy and allows bond breaking.\n"
    "\n"
    "def morse_energy(r, D=1.0, alpha=1.5, r0=1.0):\n"
    "    return D * (1 - np.exp(-alpha * (r - r0)))**2 - D\n"
    "\n"
    "r = np.linspace(0.7, 4.0, 500)\n"
    "v_lj    = 4.0 * (r**-12 - r**-6)\n"
    "v_morse = morse_energy(r, D=1.0, alpha=1.8, r0=2**(1/6))\n"
    "\n"
    "fig, axes = plt.subplots(1, 2, figsize=(11, 4))\n"
    "\n"
    "ax = axes[0]\n"
    "ax.plot(r, np.clip(v_lj, -1.5, 3), label='Lennard-Jones', lw=2.2)\n"
    "ax.plot(r, v_morse, label='Morse (bond-like)', lw=2.2, ls='--')\n"
    "ax.axhline(0, color='gray', lw=0.6)\n"
    "ax.set_ylim(-1.5, 3); ax.set_xlim(0.7, 4.0)\n"
    "ax.set_xlabel('distance r'); ax.set_ylabel('V(r)')\n"
    "ax.set_title('Two pair potentials'); ax.legend()\n"
    "\n"
    "ax = axes[1]\n"
    "ax.plot(r, np.clip(v_lj, -1.5, 1), label='Lennard-Jones', lw=2.2)\n"
    "ax.plot(r, v_morse, label='Morse (bond-like)', lw=2.2, ls='--')\n"
    "ax.axhline(0, color='gray', lw=0.6)\n"
    "ax.set_xlim(0.9, 3.5); ax.set_ylim(-1.4, 0.5)\n"
    "ax.set_xlabel('distance r'); ax.set_ylabel('V(r)')\n"
    "ax.set_title('Well shape: key difference')\n"
    "ax.legend()\n"
    "\n"
    "plt.tight_layout()\n"
    "plt.show()\n"
    "\n"
    "print('Morse goes to 0 as r -> infinity (bond can break).')\n"
    "print('LJ also goes to 0, but its well depth is tied to its repulsion --')\n"
    "print('not an independent, tunable dissociation energy.')"
)

# ---------------------------------------------------------------------------
md(
    "## 2. Failure mode 1: bonds do not break\n"
    "\n"
    "The most important thing a chemistry potential must do is allow **bonds to form and break** "
    "with the right energetics. Chemical reactions are nothing else.\n"
    "\n"
    "LJ has a well at $r = 2^{1/6}\\sigma$, so it *does* hold two atoms together and *does* resist "
    "being pulled apart: like any pair potential with a well, its tensile force rises as you "
    "separate the atoms, peaks at the inflection of $V$ (for LJ near $r \\approx 1.24\\,\\sigma$), "
    "and then decays back to zero once they are far apart. Morse behaves exactly the same way -- "
    "so the difference between them is **not** that one has a tensile-force peak and the other "
    "does not. Both do, as the force plot below shows.\n"
    "\n"
    "The real difference is in the *shape and tunability* of the well, and that is what makes LJ "
    "a model of van der Waals attraction rather than a chemical bond:\n"
    "\n"
    "- **The dissociation energy is not a free parameter.** LJ's well depth $\\varepsilon$ is the "
    "same $\\varepsilon$ that sets the repulsion, so you cannot choose a bond strength "
    "independently. Morse's $D$ *is* the dissociation energy, tuned on its own.\n"
    "- **The attractive tail is the wrong physics for a bond.** LJ decays as a power law $r^{-6}$ "
    "(long-range van der Waals, correct for closed-shell noble gases); a covalent bond is screened "
    "and decays exponentially, which is what Morse does.\n"
    "- **The repulsion is artificially stiff.** The $r^{-12}$ wall is a computational convenience, "
    "not physics; real repulsion is softer.\n"
    "\n"
    "So LJ is not 'missing the bond-breaking peak' -- it is using van-der-Waals energetics where "
    "chemistry needs a tunable, exponentially-screened bond."
)

code(
    "r = np.linspace(0.9, 4.0, 500)\n"
    "\n"
    "v_lj    = 4.0 * (r**-12 - r**-6)\n"
    "v_morse = morse_energy(r, D=1.0, alpha=1.8, r0=2**(1/6))\n"
    "\n"
    "f_lj    = -np.gradient(v_lj, r)\n"
    "f_morse = -np.gradient(v_morse, r)\n"
    "\n"
    "fig, axes = plt.subplots(1, 2, figsize=(11, 4))\n"
    "\n"
    "ax = axes[0]\n"
    "ax.plot(r, v_lj,    label='LJ potential',    lw=2)\n"
    "ax.plot(r, v_morse, label='Morse potential', lw=2, ls='--')\n"
    "ax.axhline(0, color='gray', lw=0.5)\n"
    "ax.set_xlim(0.9, 4.0); ax.set_ylim(-1.4, 2)\n"
    "ax.set_xlabel('r'); ax.set_ylabel('V(r)'); ax.set_title('Energy'); ax.legend()\n"
    "\n"
    "ax = axes[1]\n"
    "ax.plot(r, f_lj,    label='LJ force',    lw=2)\n"
    "ax.plot(r, f_morse, label='Morse force', lw=2, ls='--')\n"
    "ax.axhline(0, color='gray', lw=0.5)\n"
    "ax.set_xlim(0.9, 4.0); ax.set_ylim(-5, 5)\n"
    "ax.set_xlabel('r'); ax.set_ylabel('F(r) = -dV/dr')\n"
    "ax.set_title('Force along separation')\n"
    "ax.legend()\n"
    "\n"
    "plt.tight_layout()\n"
    "plt.show()\n"
    "\n"
    "r_inflection_morse = 2**(1/6) + np.log(2)/1.8\n"
    "r_inflection_lj    = (26/7)**(1/6)          # inflection of V_LJ, in units of sigma\n"
    "print(f'Morse max tensile force near r ~ {r_inflection_morse:.2f}')\n"
    "print(f'LJ    max tensile force near r ~ {r_inflection_lj:.2f}  (LJ has a peak too)')\n"
    "print('Both dissociate to zero; the real differences are the tunable well depth')\n"
    "print('and the tail shape (LJ ~ r^-6 van der Waals vs Morse ~ exponential bond).')"
)

