md(
    "## 4. The transferability problem\n"
    "\n"
    "Even if we accept all the approximations, classical force fields hit a fundamental wall: "
    "**the parameters fit for one molecule do not transfer to another**.\n"
    "\n"
    "We can tune the O-H spring constant to make the bond vibrate at the right frequency "
    "in an isolated water molecule. But in liquid water, each molecule interacts with its "
    "neighbours and those interactions subtly change the bond's response. In an enzyme "
    "active site, the same water molecule sits next to a metal ion and a charged amino acid -- "
    "everything changes again.\n"
    "\n"
    "Classical force fields patch this by adding more and more terms -- polarisability, "
    "charge-on-spring, multipoles -- and re-fitting parameters for every new environment. "
    "This works but does not scale. Each new material requires a new fit.\n"
    "\n"
    "Let us make this concrete by comparing what different functional forms are needed "
    "for noble gases, ionic materials, and metals."
)

code(
    "r = np.linspace(1.5, 6.0, 400)\n"
    "\n"
    "# Noble gas (argon): LJ is essentially correct\n"
    "v_lj = 4.0 * (r**-12 - r**-6)\n"
    "\n"
    "# Buckingham potential -- alternative for ionic materials\n"
    "# V = A*exp(-B*r) - C/r^6  (softer repulsion, used for NaCl etc.)\n"
    "A, B_buck, C = 8000.0, 4.5, 80.0\n"
    "v_buck = A * np.exp(-B_buck * r) - C / r**6\n"
    "v_buck_norm = v_buck / max(abs(v_buck.min()), 1e-9)  # normalize for display\n"
    "\n"
    "# Finnis-Sinclair style (metals): embedding energy depends on sqrt of density\n"
    "# This is a MANY-BODY effect -- cannot be expressed as a sum of pair terms\n"
    "rho   = np.exp(-2 * (r - 2.0))\n"
    "v_metal = -np.sqrt(rho) + 0.5 * np.exp(-4 * (r - 2.0))\n"
    "v_metal_norm = v_metal / abs(v_metal.min())\n"
    "\n"
    "fig, axes = plt.subplots(1, 3, figsize=(14, 4))\n"
    "\n"
    "configs = [\n"
    "    ('Noble gas (argon)\\nLJ works well',     v_lj,          'argon -- good fit'),\n"
    "    ('Ionic material\\nBuckingham form',       v_buck_norm,   'ionic -- needs tuning'),\n"
    "    ('Metal (Fe, Cu ...)\\nMany-body effect',  v_metal_norm,  'metal -- pair pot. fails'),\n"
    "]\n"
    "\n"
    "for ax, (label, v, note) in zip(axes, configs):\n"
    "    ax.plot(r, np.clip(v, -1.5, 2.0), lw=2.2)\n"
    "    ax.plot(r, np.clip(v_lj, -1.5, 2.0), lw=1.5, ls=':', color='gray', label='LJ (ref)')\n"
    "    ax.axhline(0, color='k', lw=0.4)\n"
    "    ax.set_xlim(1.5, 6.0); ax.set_ylim(-1.5, 2.0)\n"
    "    ax.set_xlabel('r'); ax.set_ylabel('V(r) (norm.)')\n"
    "    ax.set_title(label + f'\\n[{note}]')\n"
    "    ax.legend(fontsize=8)\n"
    "\n"
    "plt.tight_layout()\n"
    "plt.show()\n"
    "\n"
    "print('argon : LJ is a reasonable model (spherical, van der Waals)')\n"
    "print('ionic : need Coulomb terms and different repulsion -- LJ is wrong in detail')\n"
    "print('metal : many-body embedding energy; a pair potential cannot capture')\n"
    "print('        the difference between a surface atom and a bulk atom.')\n"
    "print()\n"
    "print('One formula cannot rule them all.')"
)

