md(
    "## 3. Failure mode 2: no directionality\n"
    "\n"
    "Water (H2O) is the most important molecule on Earth. "
    "Its two O-H bonds form an angle of about 104.5 degrees. "
    "That angle is why water is a liquid at room temperature, "
    "why it has high surface tension, why it is a good solvent.\n"
    "\n"
    "LJ has no concept of angles. Every atom interacts with every other by the same "
    "spherical formula, regardless of direction.\n"
    "\n"
    "To model water classically, chemists add explicit **angle and bond terms** to the energy:\n"
    "\n"
    "$$V_{FF} = \\sum_{bonds} k_b (r - r_0)^2 + \\sum_{angles} k_{\\theta} (\\theta - \\theta_0)^2"
    " + \\sum_{pairs} V_{LJ}(r) + \\sum_{pairs} \\frac{q_i q_j}{r}$$\n"
    "\n"
    "This is a **force field** -- a recipe with many hand-tuned parameters. "
    "Let us implement a minimal version to see what this buys us -- and where it still breaks."
)

code(
    "# Minimal water geometry (SPC-like model, illustrative only)\n"
    "K_BOND  = 502.0          # spring constant (kJ/mol/nm^2)\n"
    "R0_OH   = 0.1            # equilibrium O-H bond length (nm)\n"
    "K_ANGLE = 628.0          # angle spring constant (kJ/mol/rad^2)\n"
    "THETA0  = np.radians(109.47)   # H-O-H equilibrium angle (tetrahedral)\n"
    "\n"
    "def water_angle(pos_O, pos_H1, pos_H2):\n"
    "    v1 = pos_H1 - pos_O\n"
    "    v2 = pos_H2 - pos_O\n"
    "    cos_theta = np.dot(v1, v2) / (np.linalg.norm(v1) * np.linalg.norm(v2))\n"
    "    return np.arccos(np.clip(cos_theta, -1, 1))\n"
    "\n"
    "def water_energy(pos_O, pos_H1, pos_H2):\n"
    "    r_OH1 = np.linalg.norm(pos_H1 - pos_O)\n"
    "    r_OH2 = np.linalg.norm(pos_H2 - pos_O)\n"
    "    theta = water_angle(pos_O, pos_H1, pos_H2)\n"
    "    E_bond  = 0.5 * K_BOND  * ((r_OH1 - R0_OH)**2 + (r_OH2 - R0_OH)**2)\n"
    "    E_angle = 0.5 * K_ANGLE * (theta - THETA0)**2\n"
    "    return E_bond + E_angle\n"
    "\n"
    "# Place a water molecule at equilibrium\n"
    "O  = np.array([0.0, 0.0])\n"
    "H1 = np.array([ R0_OH * np.sin(THETA0/2), -R0_OH * np.cos(THETA0/2)])\n"
    "H2 = np.array([-R0_OH * np.sin(THETA0/2), -R0_OH * np.cos(THETA0/2)])\n"
    "\n"
    "print(f'Equilibrium energy: {water_energy(O, H1, H2):.6f}  (should be ~0)')\n"
    "print(f'H-O-H angle: {np.degrees(water_angle(O, H1, H2)):.1f} deg  (target: {np.degrees(THETA0):.1f} deg)')\n"
    "\n"
    "# Visualize\n"
    "fig, axes = plt.subplots(1, 2, figsize=(11, 4))\n"
    "\n"
    "ax = axes[0]\n"
    "for H in [H1, H2]:\n"
    "    ax.plot([O[0], H[0]], [O[1], H[1]], 'k-', lw=2, zorder=1)\n"
    "ax.scatter(*O,  s=800, color='red',   label='O', zorder=2, edgecolors='k')\n"
    "ax.scatter(*H1, s=300, color='white', label='H', zorder=2, edgecolors='k')\n"
    "ax.scatter(*H2, s=300, color='white', zorder=2, edgecolors='k')\n"
    "ax.set_aspect('equal')\n"
    "ax.set_title(f'Water molecule (angle = {np.degrees(THETA0):.1f} deg)')\n"
    "ax.legend(); ax.axis('off')\n"
    "\n"
    "# Energy landscape: vary the H-O-H angle\n"
    "angles_deg = np.linspace(60, 180, 200)\n"
    "E_scan = [\n"
    "    water_energy(\n"
    "        O,\n"
    "        np.array([ R0_OH * np.sin(np.radians(a)/2), -R0_OH * np.cos(np.radians(a)/2)]),\n"
    "        np.array([-R0_OH * np.sin(np.radians(a)/2), -R0_OH * np.cos(np.radians(a)/2)])\n"
    "    ) for a in angles_deg\n"
    "]\n"
    "\n"
    "ax = axes[1]\n"
    "ax.plot(angles_deg, E_scan, lw=2.2)\n"
    "ax.axvline(np.degrees(THETA0), color='crimson', ls='--',\n"
    "           label=f'target {np.degrees(THETA0):.0f} deg')\n"
    "ax.set_xlabel('H-O-H angle (deg)'); ax.set_ylabel('energy (arb. units)')\n"
    "ax.set_title('Angle energy landscape: one harmonic bowl')\n"
    "ax.legend()\n"
    "\n"
    "plt.tight_layout()\n"
    "plt.show()"
)

